package day0304;

public class MoltiTable {
    public static void main(String[] args) {

        for (int num = 1;num<=9; num++){//行
            for (int i = 1; i <=num; i++) {//列

                System.out.print(i + "*" + num + "=" + i * num + "\t");
            }
            System.out.println();
        }













































    }




}


