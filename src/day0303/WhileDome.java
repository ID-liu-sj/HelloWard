package day0303;

public class WhileDome {
    public static void main(String[] args) {
        int num = 2;//
        while (num<=9){
            System.out.println(num+"*5="+num*5);
            num+=2;


        }
        System.out.println("继续执行");

        //num=2 <9 boolean true 输出 num=4
        //num=4<9 boolean true 输出  num=6
        //num=6<9 boolean true 输出 num=8
        //num=8<9 boolean true 输出 num=10
        //num=10>9 boolean  false while结束








        int times = 0;
        while (times<9){
            System.out.println("下班喝PP");
            times++;


        }

        System.out.println("继续喝");

        //times=0<9 boolean true 输出 times=1
        //times=1<9 boolean true 输出 times=2
        //times=2<9 boolean true 输出 times=3
        //times=3<9 boolean true 输出 times=4
        //times=4<9 boolean true 输出 times=5
        //times=5<9 boolean true 输出 times=6
        //times=6<9 boolean true 输出 times=7
        //times=7<9 boolean true 输出 times=8
        //times=8<9 boolean true 输出 times=9
        //times=9=9 boolean false while结束










    }
}
